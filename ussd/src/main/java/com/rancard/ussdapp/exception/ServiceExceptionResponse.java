package com.rancard.ussdapp.exception;

public class ServiceExceptionResponse {
    private int code;
    private String message;

    public ServiceExceptionResponse(int code, String message) {
        this.code = code;
        this.message = message;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

}
